using System;

namespace Example
{
    class Program
    {
        // 「ありがとうございました!」と表示するメソッドを定義
        static void Thankyou()
        {
            Console.WriteLine("ありがとうございました!");
        }

        static void Main(string[] args)
        {
            // Thankyouメソッドを呼び出す
            Thankyou();
        }
    }
}